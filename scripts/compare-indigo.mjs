for (const fund of ['ETH', 'SOL']) {
  console.log(`\n=== ${fund} Per-Snapshot Comparison ===`);
  const pMap = {};
  for (const [d, v] of platform[fund]) pMap[d] = v;

  let lastDiff = new Decimal(0);
  for (const [exDate, exVal] of excel[fund]) {
    const e = new Decimal(exVal);

    // Try exact match and +-1 day
    let pVal = null, matchDate = exDate;
    if (pMap[exDate]) { pVal = pMap[exDate]; }
    else {
      const d = new Date(exDate);
      for (const off of [-1, 1]) {
        const d2 = new Date(d.getTime() + off * 86400000);
        const ds = d2.toISOString().slice(0, 10);
        if (pMap[ds]) { pVal = pMap[ds]; matchDate = ds; break; }
      }
    }

    if (!pVal) {
      console.log(`  ${exDate}: Excel=${e.toFixed(10)} | NO PLATFORM EVENT (Excel-only event)`);
      continue;
    }

    const p = new Decimal(pVal);
    const diff = p.minus(e);
    const deltaDiff = diff.minus(lastDiff);
    lastDiff = diff;
    const sign = diff.isPositive() ? '+' : '';
    const ds = deltaDiff.isPositive() ? '+' : '';

    const absDiff = diff.abs();
    const status = absDiff.lt('0.000001') ? 'MATCH' : 'DIVERGE';

    console.log(`  ${exDate}(p:${matchDate}): ${status}  diff=${sign}${diff.toFixed(12)}  step=${ds}${deltaDiff.toFixed(12)}`);
  }
}
